// Define the numbers and the operation here
const x = 5;
const y = 3;
const operation = "*";

// Write if-statements here
if (operation === "+") {
  console.log(x + y);
} else if (operation === "-") {
  console.log(x - y);
} else if (operation === "*") {
  console.log(x * y);
} else if (operation === "/") {
  console.log(x / y);
}
